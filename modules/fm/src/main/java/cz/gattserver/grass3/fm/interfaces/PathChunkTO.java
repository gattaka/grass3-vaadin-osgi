package cz.gattserver.grass3.fm.interfaces;

public class PathChunkTO {

	private String name;
	private String path;

	public PathChunkTO(String name, String path) {
		this.name = name;
		this.path = path;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

}
